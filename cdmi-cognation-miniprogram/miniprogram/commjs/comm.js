function findRelation(cognation, relation) {
    var newrelation = {};
    for (var i = 0; i < relations.length; i++) {
        if (relations[i].key == cognation) {
            var titles = relations[i].titles;
            for (var j = 0; j < titles.length; j++) {
                if (titles[j].key == relation) {
                    newrelation.key = titles[j].key;
                    newrelation.value = titles[j].value;
                    newrelation.sex = titles[j].sex;
                    newrelation.antonym = titles[j].antonym;
                    return newrelation;
                }
            }
        }
    };
    return null;
};

function findAntonymRelation(relation, sex) {
    if (relation == null) {
        return null;
    }
    var old_relation = {};
    for (var i = 0; i < relations.length; i++) {
        var titles = relations[i].titles;
        for (var j = 0; j < titles.length; j++) {
            if (titles[j].key == relation) {
                old_relation.key = titles[j].key;
                old_relation.value = titles[j].value;
                old_relation.sex = titles[j].sex;
                old_relation.antonym = titles[j].antonym;
                break;
            }
        }
        if (old_relation.key != null) {
            break;
        }
    };
    var new_relation = {};
    for (var i = 0; i < relations.length; i++) {
        var titles = relations[i].titles;
        for (var j = 0; j < titles.length; j++) {
            if (old_relation.antonym.length = 1) {
                if (titles[j].key == old_relation.antonym[0]) {
                    new_relation.key = titles[j].key;
                    new_relation.value = titles[j].value;
                    new_relation.sex = titles[j].sex;
                    return new_relation;
                }
            } else if (old_relation.antonym.length = 2) {
                if ((titles[j].key == old_relation.antonym[0]) || (titles[j].key == old_relation.antonym[1])) {
                    if (titles[j].sex != sex) {
                        new_relation.key = titles[j].key;
                        new_relation.value = titles[j].value;
                        new_relation.sex = titles[j].sex;
                        return new_relation;
                    }
                }
            }
        }
    };
    return null;
};

